    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        int edad = Integer.valueOf(txtEdad.getText());
        String correo = txtCorreo.getText();
        String contrasenia = txtContrsenia.getText();
        String cursoAss = txtCursoAs.getText();
        String telefono = txtTelefono.getText();
        int cbx = jCbxTipoNum.getSelectedIndex();

        if (!cedula.isBlank() || !nombre.isBlank() || !apellido.isBlank() || !txtEdad.getText().isBlank() || !correo.isBlank() || !contrasenia.isBlank() || !cursoAss.isBlank() || !telefono.isBlank() || cbx > 0) {
            if (telefono(telefono) && cedula(cedula)) {
//                System.out.println(ctrlRector.comprobarCorreo(correo, ruta));
                if (ctrlDocente.comprobarCorreo(correo) && !listar(correo)) {
                    /////
                    try {
                        var docente = new Docente(cursoAss, cedula, nombre, apellido, edad, telefono, correo, contrasenia);
                        ctrlDocente.create(docente);
                        ctrlRector.obtenerSesionInciada().createDocente(docente);
                        ctrlDocente.guardardatos(ruta2);
                        ctrlRector.guardardatos(ruta);
                        try {
                            System.out.println("**************************************\n"
                                    + ctrlDocente.cargarDatos(ruta2));
                        } catch (FileNotFoundException | ClassNotFoundException ex) {
                            Logger.getLogger(VntGestionarDocentes.class.getName()).log(Level.SEVERE, null, ex);
                        }

                        JOptionPane.showMessageDialog(this, "Registrado correctamente");
                        limpiar();
                    } catch (IOException ex) {
                        Logger.getLogger(VntGestionarDocentes.class.getName())
                                .log(Level.SEVERE, null, ex);
                    }
                    //////
//                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Este correo ya fue usado");
                    txtCorreo.setText("");
                }
            } else {
                if (!telefono(telefono)) {
                    txtTelefono.setText("");
                }
                if (!cedula(cedula)) {
                    txtCedula.setText("");
                }
                JOptionPane.showMessageDialog(this, "Los datos de algun campo estan mal ingresados");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Hay campos vacios por llenar");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    public boolean listar(String correo) {
        try {
            var list = ctrlRector.cargarDatos(ruta);
            for (int i = 0; i < list.size(); i++) {
                var get = list.get(i);
                System.out.println("----->" + get.getCorreo());
                if (correo.equals(get.getCorreo())) {
                    return true;
                }
            }
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(VntGestionarDocentes.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }

