    public boolean telefono(String telefono) {
        if (jCbxTipoNum.getSelectedItem().equals("Casa")) {
            System.out.println("casa");
            Pattern tipo = Pattern.compile("^(4|2)?\\d{6}$");
            Matcher m = tipo.matcher(telefono);
            System.out.println(m.matches());
            return m.matches();
        } else if (jCbxTipoNum.getSelectedItem().equals("Movil")) {
            System.out.println("movil");
            Pattern tipo = Pattern.compile("^(09)?\\d{8,8}$");
            Matcher m = tipo.matcher(telefono);
            System.out.println(m.matches());
            return m.matches();
        }
        return false;
    }

    public boolean cedula(String numCedula) {
        Pattern p = Pattern.compile("\\d{10,10}");
        Matcher m = p.matcher(numCedula);
        return m.matches();
    }

    public boolean listar(String correo) {
        try {
            var listRector = ctrlRector.cargarDatos(ruta);
            var listDoc = ctrlDocente.cargarDatos(ruta2);
            for (int i = 0; i < listRector.size(); i++) {
                    var rector = listRector.get(i);
                for (int j = 0; j < listDoc.size(); j++) {
                    var docente = listDoc.get(j);
                    if (correo.equals(rector.getCorreo()) || correo.equals(docente.getCorreo())) {
                        return true;
                    }
                }
            }
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(VntGestionarDocentes.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }
    
