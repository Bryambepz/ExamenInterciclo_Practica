    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String cedula = txtCedula.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        int edad = Integer.valueOf(txtEdad.getText());
        String correo = txtCorreo.getText();
        String contrasenia = txtContrsenia.getText();
        String cursoAsignado = txtCurso.getText();
        String telefono = txtTelefono.getText();
        int cbx = jCbxTipoNum.getSelectedIndex();
        ///
        String Recedula = txtCedula1.getText();
        String Renombre = txtNombre1.getText();
        String Reapellido = txtApellido1.getText();
        int Reedad = Integer.valueOf(txtEdad1.getText());
        String Recorreo = txtCorreo1.getText();
        String Recontrasenia = txtContrsenia1.getText();
        String Retelefono = txtTelefono1.getText();
        int Recbx = jCbxTipoNum1.getSelectedIndex();
        if (!cedula.isBlank() || !nombre.isBlank() || !apellido.isBlank() || !txtEdad.getText().isBlank() || !correo.isBlank() || !contrasenia.isBlank() || !cursoAsignado.isBlank() || !telefono.isBlank() || cbx > 0
                || !Recedula.isBlank() || !Renombre.isBlank() || !Reapellido.isBlank() || !txtEdad1.getText().isBlank() || !Recorreo.isBlank() || !Recontrasenia.isBlank() || !Retelefono.isBlank() || Recbx > 0) {
            if (telefono(telefono) && cedula(cedula) && telefono(Retelefono) && cedula(Recedula)) {
                
                if (ctrlEstudiantep.comprobarCorreo(correo) && ctrlEstudiantep.comprobarCorreo(Recorreo) && !listar(correo)) {
                    try {
                        try {
                            var estudiante = new Estudiante(Recedula, Renombre, Reapellido, Retelefono, Reedad, Recorreo, Recontrasenia, cursoAsignado, cedula, nombre, apellido, edad, telefono, correo, contrasenia);
                            ctrlEstudiantep.create(estudiante);
                            ctrlDocente.obtenerSesionInciada().createEstudiante(estudiante);
//                            ctrlRector.obtenerSesionInciada().createEstudiante(estudiante);
                            ctrlEstudiantep.guardardatos(ruta3);
                            ctrlDocente.guardardatos(ruta2);
                            System.out.println("**************************************\n"
                                    + ctrlEstudiantep.cargarDatos(ruta3));
                            JOptionPane.showMessageDialog(this, "Registrado correctamente");
                            limpiar();
                        } catch (FileNotFoundException | ClassNotFoundException ex) {
                            Logger.getLogger(VntGestionarEstudiantes.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    } catch (IOException ex) {
                        Logger.getLogger(VntGestionarEstudiantes.class.getName()).log(Level.SEVERE, null, ex);
                    }
//                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Este correo ya fue usado");
                    if (!ctrlEstudiantep.comprobarCorreo(correo)) {
                        txtCorreo.setText("");
                    }else if (!ctrlEstudiantep.comprobarCorreo(Recorreo)){
                        txtCorreo1.setText("");
                    }
                }
            } else {
                if (!telefono(telefono) && !telefono(Retelefono)) {
                    txtTelefono.setText("");
                }
                if (!cedula(cedula) && !cedula(Recedula)) {
                    txtCedula.setText("");
                }
                JOptionPane.showMessageDialog(this, "Los datos de algun campo estan mal ingresados");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Hay campos vacios por llenar");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

